package JavaBasicsAssignment;

public class QuotientRemainder {
    public static void main(String[] args){
        int a = 100;
        int b = 5;
        int quotient = a / b;
        int remainder = a % b;
        System.out.println("Quotient is: " + quotient);
        System.out.println("Remainder is: " + remainder);
    }
}

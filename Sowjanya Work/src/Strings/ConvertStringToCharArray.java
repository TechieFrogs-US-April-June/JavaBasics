package Strings;

public class ConvertStringToCharArray {
    public static void main(String[] args) {

        String s = new String("jyfvhshdytfGHFUK");
        char[] c =s.toCharArray();

        System.out.println(c);

        for(char temp:c)
        {
            System.out.println(temp);
        }
    }
}

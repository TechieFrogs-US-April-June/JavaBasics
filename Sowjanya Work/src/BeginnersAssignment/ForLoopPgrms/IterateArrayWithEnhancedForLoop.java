package BeginnersAssignment.ForLoopPgrms;

public class IterateArrayWithEnhancedForLoop {

    public static void main(String args[]){
        int arr[]={2,11,45,9};
        for (int num : arr) {
            System.out.println(num);
        }

        String ar[]={"hi","hello","bye"};
        for (String str : ar) {
            System.out.println(str);
        }
    }
}

package BeginnersAssignment.Operators;

public class BasicArithmeticOperators {
    public static void main(String[] args) {

        int num1 = 100;
        int num2 = 25;

        System.out.println("num1 + num2: " + (num1 + num2) );
        System.out.println("num1 - num2: " + (num1 - num2) );
        System.out.println("num1 * num2: " + (num1 * num2) );
        System.out.println("num1 / num2: " + (num1 / num2) );
        System.out.println("num1 % num2: " + (num1 % num2) );




    }
}

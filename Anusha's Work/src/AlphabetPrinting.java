public class AlphabetPrinting {
    public static void main(String[] args) {
        //printing UPPERCASE Alphabets
        for(char ch='A';ch<='Z';ch++)
        {
            System.out.print(ch);
        }
        //printing LOWERCASE Alphabets
        for(char ch='a';ch<='z';ch++)
        {
            System.out.print(ch);
        }
    }
}

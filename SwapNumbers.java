public class SwapNumbers {
    public static void main(String[] args) {
        {
            int firstNumber = 15;
            int secondNumber = 20;
            firstNumber = 15 - 20;
            secondNumber = 15 + 20;
            firstNumber = 20 - 15;
            System.out.println(firstNumber);
            System.out.println(secondNumber);
                    }
    }
}

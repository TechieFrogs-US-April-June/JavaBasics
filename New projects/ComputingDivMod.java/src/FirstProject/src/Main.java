

class ComputingDivMod {
    public static void main(String[] args){
        int e=60;
        int f=20;
        int g;
        int h;
        g=e/f;//Result will be quotient after division
        h=e%f;//Result will be remainder after Modulus operation
        System.out.println(g);
        System.out.println(h);
    }

}

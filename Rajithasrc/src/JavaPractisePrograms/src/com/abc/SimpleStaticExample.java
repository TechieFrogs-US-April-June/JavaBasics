package com.abc;

public class SimpleStaticExample {

	
		 static void myMethod()
		    {
		        System.out.println("myMethod");
		    }
		 
		    public static void main(String[] args)
		    {
		          /* You can see that we are calling this
		           * method without creating any object. 
		           */
		           myMethod();

	}

}

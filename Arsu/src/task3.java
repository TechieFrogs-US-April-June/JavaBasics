// 3.	Java Program to swap numbers
class task3
{
 public static void main(String args[]) {
       int a = 70;
       int b = 60;
    System.out.println ("a value is =" + a   +"  b value is =" + b);
    System.out.println("swaping a and b value");
    int c;
    c=a-a+b;
    b=b-b+a;
    System.out.println("a value is =" + c   +"  b value is =" + b);
  }

}

public class IncDecOperators {
    public static void main(String[] args){
        int i = 5;
        int x = 10;

        //Increment Operator
        System.out.println(i++);
        System.out.println(i);
        System.out.println(++i);
        System.out.println(i);


        System.out.println(--x);
        System.out.println(x);
        System.out.println(x--);
        System.out.println(x);


    }
}

package com.test.string;

public class Address {
	int hno;
	String loc;
	public Address(int hno, String loc) {
		
		this.hno = hno;
		this.loc = loc;
	}
	public int getHno() {
		return hno;
	}
	public void setHno(int hno) {
		this.hno = hno;
	}
	public String getLoc() {
		return loc;
	}
	public void setLoc(String loc) {
		this.loc = loc;
	}
	
	

}

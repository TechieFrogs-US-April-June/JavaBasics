package com.operators;

public class LogicalOperators {

	public static void main(String[] args) {
		//logical operators(&&,||,!)
		boolean b1 = true;
		boolean b2 =false;
		System.out.println("b1&&b2 is " +(b1&&b2));
		System.out.println("b1||b2 is " +(b1||b2));
		System.out.println("b1!=b2 is " +(b1!=b2));
	}

}

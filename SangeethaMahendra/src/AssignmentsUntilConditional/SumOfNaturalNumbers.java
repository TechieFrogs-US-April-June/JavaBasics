

public class SumOfNaturalNumbers {
    public static void main(String[] args) {
        int n = 10;
        int n1 = 1;
        int i;  
        for (i = 0; i<=n; i++)
        {
            n1 = n1 + i;
            System.out.println(i);
            System.out.println("Factorial of a Number " + n1);
            
        }
        
    }
    
}

package src.Arrays;

public class StringArrayDemo {
    public static void main(String[] args) {
        // ITERATE STRING ARRAY 
        String s[] = {"Tom", "Max", "Jim"};
        for(String t:s)
        {
            System.out.println(t);
        }

         // ITERATE CHAR ARRAY 
         char c[] = {'T','o','m'};
         for(char c1:c)
         {
             System.out.println(c1);
         }
    }
    
}

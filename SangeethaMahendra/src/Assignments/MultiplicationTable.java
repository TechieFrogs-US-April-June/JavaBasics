

public class MultiplicationTable {
    public static void main(String[] args) {
        
        int j = 12;
        int Multiply = 1;
        for(int i=1; i<=10; i++)
        {
           Multiply = i * j;
          System.out.println(j + " * " + i + " = " + Multiply );
        }
        
    }
    
}

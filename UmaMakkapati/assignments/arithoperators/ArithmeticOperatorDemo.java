package com.arithoperators;

public class ArithmeticOperatorDemo {

	public static void main(String args[]) {
	      int num1 = 100;
	      int num2 = 20;

	      System.out.println("num1 + num2: " + (num1 + num2) );
	      System.out.println("num1 - num2: " + (num1 - num2) );
	      System.out.println("num1 * num2: " + (num1 * num2) );//* is for multiplication.
	      System.out.println("num1 / num2: " + (num1 / num2) );//--/ is for division.
	      System.out.println("num1 % num2: " + (num1 % num2) ); //% is for modulo.
	      //Note: Modulo operator returns remainder, for example 10 % 5 would return 0
	   }
	}

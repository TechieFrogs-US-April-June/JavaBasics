package assignments;

public class Bitwise {

	public static void main(String[] args) {
		int a = -10;
		int b = 20;
		boolean c = true;
		boolean d =false;
		
		System.out.println(~a);//if the value is +ve then it will increase the value by 1 and it'll make it -ve
		System.out.println(~b);//if the value is -ve then it will decrease the value by 1 and it'll make it +ve
		System.out.println(!c);
		System.out.println(!d);
	}

}

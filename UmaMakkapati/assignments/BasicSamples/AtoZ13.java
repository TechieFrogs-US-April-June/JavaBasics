package com.BasicSamples;

public class AtoZ13 
{
	static char a, b;
	public static void main(String[] args) 
	{
		System.out.println("Capital letters are:");
		for (a = 'A'; a <= 'Z'; a++ )
		{
			System.out.print(a);
		}
		System.out.println();
		System.out.println("Lower case letters are: ");
		for (b = 'a'; b <= 'z'; b++)
		{
			System.out.print(b);
	   }

}
}

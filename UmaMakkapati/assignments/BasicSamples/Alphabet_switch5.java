package com.BasicSamples;

public class Alphabet_switch5 {

	public static void main(String[] args)
	{
		char alphabet  = 'b' ;
		
		switch(alphabet) {
		case 'a':
			System.out.println("It is an vowel a");
			break;
		case 'e':
			System.out.println("It ia an vowel e");
			break;
		case 'i':
			System.out.println("It is an vowel i");
			break;
		case 'o':
			System.out.println("It is an vowel o");
			break;
		case 'u':
			System.out.println("It is an vowel u");
			break;
		default:
			System.out.println("It is a consonant");
		
		}
		}

}

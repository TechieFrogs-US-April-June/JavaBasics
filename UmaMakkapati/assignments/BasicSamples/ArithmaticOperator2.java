package com.BasicSamples;

public class ArithmaticOperator2 {

	public static void main(String[] args) {
		int a = 8;
		int b = 3;
		System.out.println(a+b);
		System.out.println(b-a);
		System.out.println(a*b);
		System.out.println(b/a);
		System.out.println(a%b); 
		System.out.println(10<<2);//10*2^2=40
		System.out.println(10<<3);//10*2^3(2*2**)
		System.out.println(10>>2);//10/2^2(2*)=2
	}

}
